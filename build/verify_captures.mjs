// Appends a "Capture & Verify" section to ARRIVIO_VALIDATION_REPORT.md covering the FULL
// map dataset = properties.json (crawled) ∪ captures.json (brokers/manual). Run AFTER
// build/validate_listings.mjs (which rewrites the report from properties.json only).
//   node build/verify_captures.mjs
import { readFileSync, appendFileSync, existsSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const B = dirname(fileURLToPath(import.meta.url));
const ROOT = join(B, '..');
const rd = f => { try { return JSON.parse(readFileSync(join(ROOT, f), 'utf8')); } catch (e) { return null; } };

const props = (rd('data/properties.json') || {}).properties || [];
const caps = (rd('data/captures.json') || {}).properties || [];
const ALL = [...props, ...caps];
const assetOf = r => r.asset_type || r.kind || 'other';
const by = (arr, f) => arr.reduce((m, r) => { const k = f(r) || '—'; m[k] = (m[k] || 0) + 1; return m; }, {});
const tbl = (obj) => Object.entries(obj).sort((a, b) => b[1] - a[1]).map(([k, v]) => '`' + k + '` ' + v).join(' · ');

// baseline snapshot BEFORE this full-sweep phase (= end of Phase 3: 122 broker offices)
const BEFORE = { properties: 815, captures: 122, union: 937, portal: 46, listed: 773 };
const AFTER = { properties: props.length, captures: caps.length, union: ALL.length, portal: props.filter(r => r.source === 'pachtnetzwerk.immo' || r.source === 'gastro-pacht.de').length, listed: ALL.filter(r => r.price_basis === 'LISTED').length };

const out = [];
const log = s => out.push(s);
log('\n');
log('## Full Sweep — broker ground truth + open portals');
log('Generated by `build/verify_captures.mjs` over the FULL map dataset = `properties.json` (crawled) ∪ `captures.json` (brokers/manual). Every row keeps `source` + `source_url`; blanks where a field is not shown (never fabricated).');
log('> **Headless broker sweep status (self-check, 2026-06-28):** JLL `/search` is Akamai-blocked to the headless browser (the 316 ground-truth rows were captured in an authorized browser); E&V/Colliers/Christie sit behind cookie-consent walls; CBRE host did not resolve. `build/fetch_brokers.mjs` (self-checking, resumable) is ready to run in an authorized browser. Open portals (plain-fetch) are not blocked and were swept by `fetch_listings.mjs`.');
log('');

/* ---------- before / after ---------- */
log('### Before → after (this phase)');
log('| Dataset | Before | After | Δ |');
log('|---|--:|--:|--:|');
log('| properties.json (crawled) | ' + BEFORE.properties + ' | ' + AFTER.properties + ' | +' + (AFTER.properties - BEFORE.properties) + ' |');
log('| &nbsp;&nbsp;— of which open-portal (pachtnetzwerk + gastro-pacht) | ' + BEFORE.portal + ' | ' + AFTER.portal + ' | +' + (AFTER.portal - BEFORE.portal) + ' |');
log('| captures.json (brokers/manual) | ' + BEFORE.captures + ' | ' + AFTER.captures + ' | ' + (AFTER.captures - BEFORE.captures >= 0 ? '+' : '') + (AFTER.captures - BEFORE.captures) + ' |');
log('| **Union (pins on map)** | **' + BEFORE.union + '** | **' + AFTER.union + '** | **+' + (AFTER.union - BEFORE.union) + '** |');
log('| LISTED-price rows (real €) | ' + BEFORE.listed + ' | ' + AFTER.listed + ' | +' + (AFTER.listed - BEFORE.listed) + ' |');
log('');

/* ---------- per-source / per-asset_type ---------- */
log('### Inventory by source & asset_type (union of ' + ALL.length + ')');
log('- By source: ' + tbl(by(ALL, r => r.source)));
log('- By asset_type: ' + tbl(by(ALL, assetOf)));
log('- By deal: ' + tbl(by(ALL, r => r.deal)) + ' · by price_basis: ' + tbl(by(ALL, r => r.price_basis)));
log('- Open-portal pass: ' + tbl(by(props.filter(r => r.source === 'pachtnetzwerk.immo' || r.source === 'gastro-pacht.de'), r => r.source + '/' + r.deal)));
log('');

/* ---------- ground-truth cross-check (ALL broker rows) ---------- */
log('### Broker ground-truth cross-check (data/broker_listings_all.csv → captures.json)');
let csvCount = 0, csvIds = new Set(); const csvByType = {};
if (existsSync(join(ROOT, 'data', 'broker_listings_all.csv'))) {
  const lines = readFileSync(join(ROOT, 'data', 'broker_listings_all.csv'), 'utf8').replace(/^﻿/, '').split(/\r?\n/).slice(1).filter(l => l.trim());
  for (const ln of lines) { const c = ln.split(','); const at = (c[0] || '').trim(); if (!at) continue; csvCount++; csvByType[at] = (csvByType[at] || 0) + 1; const m = ln.match(/\b((?:jll|ev|cbre|colliers|christie)-[A-Za-z0-9]+(?:-\d+)?)\b/); if (m) csvIds.add(m[1]); }
}
const capIds = new Set(caps.map(r => r.id));
const missing = [...csvIds].filter(id => !capIds.has(id));
const capByType = by(caps, r => r.asset_type);
const perProp = caps.filter(r => /gewerbeimmobilien\.jll\.de\/(bueros|hallen|einzelhandel|grundstuecke)\//.test(r.source_url || '')).length;
const citySearch = caps.filter(r => /\/search\?/.test(r.source_url || '')).length;
const withRent = caps.filter(r => r.rent_eur_m2_mo != null).length;
const ck = (cond, s) => log((cond ? '- ✅ ' : '- ❌ ') + s);
ck(csvCount === caps.length, 'all ' + csvCount + ' CSV ground-truth rows present in captures.json (' + caps.length + ')');
ck(missing.length === 0, 'all ' + csvIds.size + ' CSV listing_ids matched (0 missing)' + (missing.length ? ' — missing: ' + missing.slice(0, 5).join(', ') : ''));
ck(JSON.stringify(csvByType) === JSON.stringify(capByType), 'asset_type counts identical CSV↔captures — CSV ' + tbl(csvByType) + ' · captures ' + tbl(capByType));
ck(citySearch === 0 && perProp === caps.length, 'source_url is the per-property page for all ' + caps.length + ' broker rows (' + perProp + ' per-property, ' + citySearch + ' city-search), e.g. ' + (caps[0] ? caps[0].source_url : ''));
ck(withRent >= caps.filter(r => r.asset_type === 'office').length * 0.9, 'rent €/m²·mo preserved (' + withRent + ' of ' + caps.length + ' rows carry a value)');
log('');

/* ---------- per-Bundesland (union) ---------- */
log('### By Bundesland (union of ' + ALL.length + ' — placed pins)');
log(Object.entries(by(ALL.filter(r => r.lat != null && r.state), r => r.state)).sort((a, b) => b[1] - a[1]).map(([k, v]) => '`' + k + '` ' + v).join(' · '));
log('- Broker rows by Bundesland: ' + tbl(by(caps.filter(r => r.state), r => r.state)));
log('');

/* ---------- sanity bounds + outliers ---------- */
log('### Price/Pacht sanity bounds (union; outliers flagged, not dropped)');
const band = (arr, lo, hi) => ({ n: arr.length, inb: arr.filter(x => x.v >= lo && x.v <= hi).length, out: arr.filter(x => x.v < lo || x.v > hi) });
const fmtOut = (o, unit, k = 0) => o.out.slice(0, 4).map(x => (x.r.city || x.r.name || '—') + ' ' + (k ? '€' + Math.round(x.v / 1000) + 'k' : '€' + x.v.toFixed(1)) + unit).join(', ') || 'none';

const officeRent = ALL.filter(r => r.rent_eur_m2_mo).map(r => ({ r, v: r.rent_eur_m2_mo }));
const b1 = band(officeRent, 3, 60);
log('- **Office/commercial rent €/m²·mo** ∈ [3,60]: ' + b1.inb + '/' + b1.n + ' in band · outliers: ' + fmtOut(b1, '/m²'));

const pachtRoom = ALL.filter(r => r.deal === 'lease' && r.lease_eur_mo && assetOf(r) === 'hotel' && r.rooms).map(r => ({ r, v: r.lease_eur_mo / r.rooms }));
const b2 = band(pachtRoom, 100, 600);
log('- **Hotel Pacht €/room·mo** ∈ [100,600] (anchor ≈185): ' + b2.inb + '/' + b2.n + ' in band · median €' + (pachtRoom.length ? pachtRoom.map(x => x.v).sort((a, b) => a - b)[pachtRoom.length >> 1].toFixed(0) : '–') + ' · outliers: ' + fmtOut(b2, '/room'));

const pachtM2 = ALL.filter(r => r.deal === 'lease' && r.lease_eur_mo && r.area_m2).map(r => ({ r, v: r.lease_eur_mo / r.area_m2 }));
const b3 = band(pachtM2, 3, 30);
log('- **Lease Pacht €/m²·mo** ∈ [3,30]: ' + b3.inb + '/' + b3.n + ' in band · outliers: ' + fmtOut(b3, '/m²'));

const eurKey = ALL.filter(r => r.deal === 'sale' && r.price_eur && r.price_basis === 'LISTED' && assetOf(r) === 'hotel' && r.rooms).map(r => ({ r, v: r.price_eur / r.rooms }));
const b4 = band(eurKey, 40000, 2000000);
log('- **Hotel €/key (sale, LISTED)** ∈ [40k,2M]: ' + b4.inb + '/' + b4.n + ' in band · outliers: ' + fmtOut(b4, '/key', 1));

const saleM2 = ALL.filter(r => r.deal === 'sale' && r.eur_per_m2 && r.price_basis === 'LISTED').map(r => ({ r, v: r.eur_per_m2 }));
const b5 = band(saleM2, 200, 20000);
log('- **Sale €/m² (LISTED)** ∈ [200,20000]: ' + b5.inb + '/' + b5.n + ' in band · outliers: ' + fmtOut(b5, '/m²', 1));
log('');
log('_Outliers are real figures from the source (often an annual sum or partial-area shown as a unit rate); surfaced here, not silently dropped._');
log('');

appendFileSync(join(ROOT, 'ARRIVIO_VALIDATION_REPORT.md'), out.join('\n') + '\n');
console.log('Appended "Full Sweep" to ARRIVIO_VALIDATION_REPORT.md');
console.log('union=' + ALL.length + ' (props ' + props.length + ' + caps ' + caps.length + ') · portal +' + AFTER.portal + ' · LISTED ' + AFTER.listed);
console.log('ground-truth cross-check: csv=' + csvCount + ' caps=' + caps.length + ' missing=' + missing.length + ' perProp=' + perProp + '/' + caps.length + ' typesMatch=' + (JSON.stringify(csvByType) === JSON.stringify(capByType)));
