// Validation + old-vs-new diff for the rebuilt supply data. Runs the 6 checks from the
// Arrivio_Source_Map.xlsx "Validation" sheet, prints a report and writes ARRIVIO_VALIDATION_REPORT.md.
//   node build/validate_listings.mjs
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const B = dirname(fileURLToPath(import.meta.url));
const ROOT = join(B, '..');
const read = f => readFileSync(join(ROOT, f), 'utf8').replace(/^﻿/, '');
const P = JSON.parse(read('data/properties.json')).properties;
const G = JSON.parse(read('data/genesis_beds_by_land.json')).data;
const COMMERCIAL_DATA = new Function('return ' + read('build/p_states.html').match(/const COMMERCIAL_DATA = (\[[\s\S]*?\]);/)[1])();
const INKAR = (() => { try { const o = JSON.parse(read('build/inflow_kreise.json')); return o.data || o; } catch (e) { return []; } })();

const out = [];
const log = s => { out.push(s); console.log(s); };
const pct = n => (Math.round(n * 1000) / 10) + '%';
const pearson = (xs, ys) => { const n = xs.length, mx = xs.reduce((a, b) => a + b, 0) / n, my = ys.reduce((a, b) => a + b, 0) / n; let sxy = 0, sx = 0, sy = 0; for (let i = 0; i < n; i++) { const dx = xs[i] - mx, dy = ys[i] - my; sxy += dx * dy; sx += dx * dx; sy += dy * dy; } return (sx && sy) ? sxy / Math.sqrt(sx * sy) : 0; };
let fails = 0; const FAIL = s => { fails++; log('   ❌ FAIL: ' + s); }; const OK = s => log('   ✅ ' + s); const WARN = s => log('   ⚠️  ' + s);

log('# Arrivio supply-data validation — ' + new Date().toISOString().slice(0, 10).replace(/T.*/, ''));
log('Generated by build/validate_listings.mjs against data/properties.json (' + P.length + ' records).\n');

/* ---------- inventory ---------- */
const by = (f) => P.reduce((m, r) => { const k = f(r) || '—'; m[k] = (m[k] || 0) + 1; return m; }, {});
log('## Inventory');
log('- Records: **' + P.length + '** (' + P.filter(r => r.lat != null).length + ' geocoded)');
log('- By source: ' + JSON.stringify(by(r => r.source)));
log('- By deal: ' + JSON.stringify(by(r => r.deal)) + ' · by basis: ' + JSON.stringify(by(r => r.price_basis)));
log('- By kind: ' + JSON.stringify(by(r => r.kind)));
log('');

/* ---------- 1. density tracks real supply (not OSM coverage) ---------- */
log('## 1. Listing density tracks real hotel supply (GENESIS beds), not OSM coverage');
const states = Object.keys(G);
const xs1 = [], ys1 = [], rows1 = [];
for (const s of states) { const cnt = P.filter(r => r.state === s).length; const beds = G[s].hotels_beds || 0; xs1.push(beds); ys1.push(cnt); rows1.push([s, cnt, beds]); }
const corr1 = pearson(xs1, ys1);
rows1.sort((a, b) => b[1] - a[1]);
log('   listings vs GENESIS hotel beds, Pearson r = **' + corr1.toFixed(2) + '**');
for (const [s, c, b] of rows1.slice(0, 5)) log('     ' + s.padEnd(22) + c + ' listings · ' + b.toLocaleString('de-DE') + ' beds');
if (corr1 >= 0.4) OK('density correlates with real supply (r ≥ 0.4)'); else FAIL('listing density does not track GENESIS beds (r=' + corr1.toFixed(2) + ')');
log('');

/* ---------- 2. vacancy vs rent inversion ---------- */
log('## 2. Office vacancy vs prime rent (expect negative correlation)');
const corr2 = pearson(COMMERCIAL_DATA.map(c => c.vac), COMMERCIAL_DATA.map(c => c.prime));
log('   vacancy% vs prime €/m², Pearson r = **' + corr2.toFixed(2) + '**');
const inversions = COMMERCIAL_DATA.filter(c => c.vac >= 11 && c.prime >= 45).map(c => c.city);
if (corr2 <= 0.05) OK('low vacancy → higher rent holds (r ≤ 0)'); else WARN('weak/positive correlation (r=' + corr2.toFixed(2) + ') — Frankfurt has both high rent & high vacancy (known market quirk)');
log('   note: tight low-vacancy markets München/Köln/Hamburg carry top rents; ' + (inversions.length ? 'high-rent+high-vacancy: ' + inversions.join(', ') : 'no gross inversions'));
log('');

/* ---------- 3. demand coherence: Berlin & Hamburg top occupancy ---------- */
log('## 3. Demand coherence — Berlin & Hamburg top hotel occupancy (exp 57%/56%)');
const occRank = states.map(s => [s, G[s].hotels_occupancy_pct]).filter(x => x[1] != null).sort((a, b) => b[1] - a[1]);
log('   top 3: ' + occRank.slice(0, 3).map(x => x[0] + ' ' + x[1] + '%').join(' · '));
const top2 = occRank.slice(0, 2).map(x => x[0]);
if (top2.includes('Hamburg') && top2.includes('Berlin')) OK('Hamburg & Berlin are the top-2 occupancy states (join is correct)'); else FAIL('Berlin/Hamburg not top-2 — GENESIS join broken');
log('');

/* ---------- 4. rural-but-connected opportunity ---------- */
log('## 4. Rural-but-connected opportunity (low rent + positive migration + listings)');
const code2state = {}; for (const s of states) code2state[G[s].land_code] = s;
const stIntl = {}; for (const r of INKAR) { const st = code2state[String(r.ags).padStart(5, '0').slice(0, 2)]; if (!st) continue; (stIntl[st] ||= []).push(r.intl); }
const A_STATES = new Set(['Berlin', 'Hamburg', 'Bayern', 'Hessen', 'Nordrhein-Westfalen', 'Baden-Württemberg']);
const anchorCities = new Set(COMMERCIAL_DATA.map(c => c.city));
const opps = states.map(s => {
  const mig = stIntl[s] ? stIntl[s].reduce((a, b) => a + b, 0) / stIntl[s].length : 0;
  const cnt = P.filter(r => r.state === s).length;
  return { s, mig: Math.round(mig * 10) / 10, cnt };
}).filter(o => o.mig > 0 && o.cnt >= 5 && !A_STATES.has(o.s)).sort((a, b) => b.mig - a.mig);
for (const o of opps.slice(0, 6)) log('     ' + o.s.padEnd(22) + 'net intl +' + o.mig + '/1.000 · ' + o.cnt + ' targets');
if (opps.some(o => o.s === 'Brandenburg')) OK('Brandenburg (Berlin-belt) flagged as opportunity, as expected'); else WARN('Brandenburg not flagged — check INKAR join');
log('');

/* ---------- 5. Pacht sanity ---------- */
log('## 5. Pacht sanity — €/m²/mo ∈ [3,30], €/room/mo ∈ [100,600] (anchor ≈185)');
const leases = P.filter(r => r.deal === 'lease' && r.lease_eur_mo);
const perM2 = leases.filter(r => r.area_m2).map(r => ({ r, v: r.lease_eur_mo / r.area_m2 }));
const perRoom = leases.filter(r => r.kind === 'hotel' && r.rooms).map(r => ({ r, v: r.lease_eur_mo / r.rooms }));
const inB = (arr, lo, hi) => arr.filter(x => x.v >= lo && x.v <= hi).length;
log('   €/m²/mo: ' + inB(perM2, 3, 30) + '/' + perM2.length + ' in band; outliers: ' + perM2.filter(x => x.v < 3 || x.v > 30).slice(0, 3).map(x => x.r.city + ' €' + x.v.toFixed(1)).join(', '));
log('   €/room/mo (hotels): ' + inB(perRoom, 100, 600) + '/' + perRoom.length + ' in band; median €' + (perRoom.length ? perRoom.map(x => x.v).sort((a, b) => a - b)[Math.floor(perRoom.length / 2)].toFixed(0) : '–'));
const roomOk = perRoom.length ? inB(perRoom, 100, 600) / perRoom.length : 1;
if (roomOk >= 0.5) OK('majority of hotel Pacht/room within sane band (anchor €185 holds)'); else WARN('many hotel Pacht/room outside [100,600] — small-room gastgewerbe inflate this');
log('');

/* ---------- 6. price sanity ---------- */
log('## 6. Price sanity — sale €/m² plausible, €/key within segment bands [40k–2M]');
const sales = P.filter(r => r.deal === 'sale' && r.price_eur && r.price_basis === 'LISTED');
const ppm = sales.filter(r => r.eur_per_m2).map(r => ({ r, v: r.eur_per_m2 }));
const perKey = sales.filter(r => r.kind === 'hotel' && r.rooms).map(r => ({ r, v: r.price_eur / r.rooms }));
log('   sale €/m²: ' + inB(ppm, 200, 20000) + '/' + ppm.length + ' in [200,20000]; outliers: ' + ppm.filter(x => x.v < 200 || x.v > 20000).slice(0, 3).map(x => x.r.city + ' €' + Math.round(x.v)).join(', '));
log('   hotel €/key: ' + inB(perKey, 40000, 2000000) + '/' + perKey.length + ' in [40k,2M]; outliers: ' + perKey.filter(x => x.v < 40000 || x.v > 2000000).slice(0, 3).map(x => x.r.city + ' €' + Math.round(x.v / 1000) + 'k').join(', '));
const keyOk = perKey.length ? inB(perKey, 40000, 2000000) / perKey.length : 1;
if (keyOk >= 0.6) OK('most hotel €/key within segment bands'); else WARN('several €/key outside bands — review flagged listings');
log('');

/* ---------- Pacht (lease) — per-hotel investor figure ---------- */
log('## Pacht (lease) — per-hotel figure (LISTED overrides the deduced range)');
const PACHT = (() => { try { return JSON.parse(read('data/pacht_model.json')); } catch (e) { return null; } })();
const leaseAll = P.filter(r => r.deal === 'lease' && r.lease_eur_mo);
const leaseHotels = leaseAll.filter(r => r.kind === 'hotel');
log('- LISTED lease records: **' + leaseAll.length + '** (' + leaseHotels.length + ' hotels) · with Nebenkosten: ' + leaseAll.filter(r => r.nk_eur_mo).length + ' · with Ablöse: ' + leaseAll.filter(r => r.abloese_eur !== undefined).length);
if (PACHT && PACHT.observed) {
  log('- observed median €/room/mo by tier (LISTED hotels): ' + ['metro', 'b_city', 'rural'].map(t => t + ' ' + (PACHT.observed.eur_per_room_mo[t].median ?? '–') + ' (n=' + PACHT.observed.eur_per_room_mo[t].n + ')').join(' · '));
  let band_ok = true;
  for (const t of ['metro', 'b_city', 'rural']) { const m = PACHT.observed.eur_per_room_mo[t].median, b = PACHT.per_room_eur_mo[t]; if (m != null && (m < b[0] * 0.6 || m > b[1] * 1.4)) { band_ok = false; WARN('tier ' + t + ' observed €/room ' + m + ' outside ~band ' + b.join('–')); } }
  if (band_ok) OK('observed €/room medians sit within/near the model bands — model is calibrated to reality');
}
if (PACHT) { log('- live estimator: Pacht ≈ 15–25% of revenue (rooms×365×occupancy×ADR; occupancy = Destatis GENESIS). Per-tier €/room reference (research-anchored): rural ' + PACHT.per_room_eur_mo.rural.join('–') + ', regional ' + PACHT.per_room_eur_mo.regional.join('–') + ', b-city ' + PACHT.per_room_eur_mo.b_city.join('–') + ', metro ' + PACHT.per_room_eur_mo.metro.join('–') + '.'); OK('Pacht method is research-backed (revenue rule + GENESIS occupancy + ADR; sources in pacht_model.json)'); }
log('');

/* ---------- old vs new diff ---------- */
log('## Old vs new — what changed');
log('| Aspect | OLD (OSM/IDW) | NEW (real listings) |');
log('|---|---|---|');
log('| Priced targets on map | 0 (none) | **' + P.filter(r => r.price_basis !== 'price_on_request').length + '** priced (LISTED+MODELED) of ' + P.length + ' |');
log('| Office rent/vacancy | IDW smear over rural land | real broker city anchors, no interpolation |');
log('| Hotel price | flat €95 star-model | LISTED Pacht/asking where present, else €/key model (flagged est) |');
log('| Hotel context | none | GENESIS beds + occupancy per Bundesland |');
log('| Office density | OSM office=* counts | unchanged (kept as baseline footprint) + real listings as new layer |');
log('| Listings with source link | 0 | ' + P.filter(r => r.source_url).length + ' (every record keeps source_url) |');
log('');
log(fails === 0 ? '**RESULT: ' + fails + ' hard failures.** Warnings are documented market quirks, not data errors.' : '**RESULT: ' + fails + ' hard failure(s) — see ❌ above.**');

writeFileSync(join(ROOT, 'ARRIVIO_VALIDATION_REPORT.md'), out.join('\n') + '\n');
console.log('\nWrote ARRIVIO_VALIDATION_REPORT.md');
process.exit(0);
